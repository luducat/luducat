    def get_metadata_for_store_game(
        self,
        store_name: str,
        store_id: str,
        normalized_title: str = "",
    ) -> Optional[Dict[str, Any]]:
        """Resolve a game in GOG's catalog and return standardized metadata.

        Resolution strategy:
        1. If store_name == "gog" → direct ID lookup
        2. Otherwise → title-based search in GOG catalog
        3. Standardize field names via _standardize_metadata()
        4. API-on-cache-miss for description and screenshots

        Args:
            store_name: Store identifier
            store_id: Store's app ID
            normalized_title: Optional normalized title for cross-store search

        Returns:
            Standardized metadata dict, or None
        """
        db = self._get_db()
        game = None

        if store_name == self.store_name:
            try:
                game = db.get_game(int(store_id))
            except (ValueError, TypeError):
                pass
        if not game and normalized_title:
            game = self._find_game_record_by_title(normalized_title)

        if not game:
            return None

        metadata = self._standardize_metadata(game.to_dict())

        # API-on-cache-miss: fetch from GOG products API if key fields empty
        if not metadata.get("description") or not metadata.get("screenshots"):
            self._fetch_missing_metadata(game, metadata)

        return metadata

    def _find_game_by_title(self, normalized_title: str) -> Optional[Dict[str, Any]]:
        """Search GOG catalog by normalized title."""
        game = self._find_game_record_by_title(normalized_title)
        if not game:
            return None
        metadata = self._standardize_metadata(game.to_dict())
        if not metadata.get("description") or not metadata.get("screenshots"):
            self._fetch_missing_metadata(game, metadata)
        return metadata

    def _find_game_record_by_title(self, normalized_title: str) -> Optional[GogGame]:
        """Find a GogGame record by normalized title using lazy index."""
        if self._title_index is None:
            self._build_title_index()
        gogid = self._title_index.get(normalized_title)
        if gogid is not None:
            return self._get_db().get_game(gogid)
        return None

    def _build_title_index(self) -> None:
        """Build lazy normalized_title → gogid index from all catalog games."""
        from luducat.plugins.sdk.text import normalize_title
        self._title_index = {}
        db = self._get_db()
        for game in db.get_all_games():
            nt = normalize_title(game.title)
            if nt:
                self._title_index[nt] = game.gogid
        logger.debug(f"Built GOG title index: {len(self._title_index)} entries")

    def _fetch_missing_metadata(
        self, game: GogGame, metadata: Dict[str, Any]
    ) -> None:
        """Fetch missing description/screenshots from GOG products API.

        Updates both the metadata dict in-place and the local DB cache.
        """
        gogid = game.gogid
        url = (
            f"https://api.gog.com/products/{gogid}"
            f"?expand=description,screenshots&locale=en-US"
        )
        headers = {"User-Agent": USER_AGENT}

        try:
            resp = self.http.get(url, headers=headers, timeout=15)
            if resp.status_code != 200:
                logger.debug(
                    f"GOG products API returned {resp.status_code} for {gogid}"
                )
                return

            data = resp.json()
            db = self._get_db()
            changed = False

            # Description
            if not metadata.get("description"):
                desc_obj = data.get("description", {})
                html_desc = desc_obj.get("full", "")
                if html_desc:
                    metadata["description"] = html_desc
                    if not game.description:
                        game.description = html_desc
                        changed = True

            # Screenshots — same parsing as gog_api.py
            if not metadata.get("screenshots"):
                screenshots = []
                for ss in data.get("screenshots", []):
                    formatted = ss.get("formatted_images", [])
                    url_str = None
                    for fmt_name in ("ggvgl_2x", "ggvgl", "ggvgm_2x", "ggvgm"):
                        for fmt in formatted:
                            if fmt.get("formatter_name") == fmt_name:
                                url_str = fmt.get("image_url")
                                break
                        if url_str:
                            break
                    if not url_str and formatted:
                        url_str = formatted[0].get("image_url")
                    if url_str:
                        screenshots.append(url_str)
                if screenshots:
                    metadata["screenshots"] = screenshots
                    if not game.screenshots:
                        game.screenshots = screenshots
                        changed = True

            if changed:
                db.commit()
                logger.info(
                    f"Enriched GOG game {gogid} from products API (on-demand)"
                )

        except Exception as e:
            logger.debug(f"Failed to fetch missing GOG metadata for {gogid}: {e}")
            try:
                self._get_db().rollback()
            except Exception:
                pass

