    async def on_sync_complete_async(
        self,
        progress_callback=None,
        cancel_check=None,
    ) -> Dict[str, Any]:
        """Async post-sync hook: enrich owned games via GOG products API.

        Enriches ALL unenriched owned games in one pass with full metadata
        (description, screenshots, downloads, changelog, DLCs, languages,
        videos).
        """
        if not self.is_authenticated():
            return {}

        db = self._get_db()
        api = self._get_api_client()

        # Get owned game IDs that haven't been enriched yet
        owned_ids = db.get_all_gogids(include_dlc=False)
        unenriched = db.get_unenriched_owned_gogids(owned_ids)

        if not unenriched:
            return {}

        total = len(unenriched)

        if progress_callback:
            progress_callback(
                f"Fetching metadata for {total} games...", 0, total
            )

        enriched = 0
        errors = 0
        for idx, gogid in enumerate(unenriched, 1):
            if cancel_check and cancel_check():
                break

            if progress_callback:
                game = db.get_game(gogid)
                name = game.title if game else str(gogid)
                progress_callback(name, idx, total)

            try:
                metadata = await api.get_product_metadata(gogid)
                if metadata:
                    existing = db.get_game(gogid)
                    if existing:
                        self._apply_products_api_data(existing, metadata)
                        db.commit()
                        enriched += 1
                else:
                    errors += 1
            except Exception as e:
                logger.warning(f"Failed to enrich GOG game {gogid}: {e}")
                db.rollback()
                errors += 1

        if enriched:
            logger.info(
                f"GOG products API enrichment: {enriched}/{total} enriched, "
                f"{errors} errors"
            )

        return {
            "gog_enriched": enriched,
            "gog_enrich_errors": errors,
        }

