    def _standardize_metadata(self, gog_meta: Dict[str, Any]) -> Dict[str, Any]:
        """Convert GOG metadata dict to standardized format

        Maps GOG-specific field names to the standard names expected by game_service.

        Args:
            gog_meta: Raw metadata from GogGame.to_dict()

        Returns:
            Metadata dict with standardized field names
        """
        # Build platforms list from boolean flags
        platforms = []
        if gog_meta.get("windows"):
            platforms.append("Windows")
        if gog_meta.get("mac"):
            platforms.append("macOS")
        if gog_meta.get("linux"):
            platforms.append("Linux")

        # Build per-platform release_date dict
        release_date_str = gog_meta.get("release_date", "")
        release_dates_dict: Dict[str, str] = {}
        if release_date_str:
            from luducat.plugins.sdk.datetime import parse_release_date
            parsed = parse_release_date(release_date_str)
            if parsed:
                if gog_meta.get("windows"):
                    release_dates_dict["windows"] = parsed
                if gog_meta.get("mac"):
                    release_dates_dict["macos"] = parsed
                if gog_meta.get("linux"):
                    release_dates_dict["linux"] = parsed

        # short_description: prefer description_lead, else generate from description
        short_desc = gog_meta.get("short_description", "")
        if not short_desc:
            desc_lead = gog_meta.get("description_lead", "")
            if desc_lead:
                short_desc = desc_lead
            elif gog_meta.get("description"):
                from luducat.plugins.base import generate_short_description
                short_desc = generate_short_description(gog_meta["description"])

        # Cover priority: catalog vertical > GOGdb boxart > CDN vertical crop
        cover = (
            gog_meta.get("cover_vertical_url")
            or _fix_gog_cdn_url(gog_meta.get("cover_url", ""))
        )
        if cover and _is_horizontal_gog_url(cover):
            cover = _force_vertical_cover(cover)

        # Background priority: galaxy > v0 background
        hero = (
            _fix_gog_cdn_url(gog_meta.get("galaxy_background_url", ""))
            or _fix_gog_cdn_url(gog_meta.get("background_url", ""))
        )

        # Languages — prefer localizations (richer), fallback to languages dict
        localizations = gog_meta.get("localizations") or []
        if localizations:
            supported_languages = [
                loc["name"] for loc in localizations
                if isinstance(loc, dict) and loc.get("text") and loc.get("name")
            ]
            full_audio_languages = [
                loc["name"] for loc in localizations
                if isinstance(loc, dict) and loc.get("audio") and loc.get("name")
            ]
        else:
            supported_languages = list((gog_meta.get("languages") or {}).keys())
            full_audio_languages = []

        # Features → game_modes_detail mapping
        features = gog_meta.get("features") or []
        game_modes_detail = {}
        controller_support = False
        _FEATURE_TO_MODE = {
            "single player": "Singleplayer",
            "multi-player": "Multiplayer",
            "co-op": "Co-op",
        }
        for feat in features:
            feat_lower = feat.lower() if isinstance(feat, str) else ""
            if feat_lower in _FEATURE_TO_MODE:
                game_modes_detail[_FEATURE_TO_MODE[feat_lower]] = True
            elif "controller" in feat_lower:
                controller_support = True

        # Links consolidation — build normalized list of {"type": ..., "url": ...}
        stored_links = gog_meta.get("links") or []
        # Convert list to dict for dedup/merge, then back to list
        links_dict = {entry["type"]: entry["url"] for entry in stored_links if isinstance(entry, dict) and entry.get("url")}
        slug = gog_meta.get("slug", "")
        store_link = gog_meta.get("store_link")
        if store_link:
            if store_link.startswith("/"):
                links_dict["store"] = f"https://www.gog.com{store_link}"
            else:
                links_dict["store"] = store_link
        elif slug and "store" not in links_dict:
            links_dict["store"] = f"https://www.gog.com/game/{slug}"
        forum_link = gog_meta.get("forum_link")
        if forum_link:
            links_dict["forum"] = forum_link
        support_link = gog_meta.get("support_link")
        if support_link:
            links_dict["support"] = support_link
        links_list = [{"type": k, "url": v} for k, v in links_dict.items() if v]

        # Content ratings → required_age extraction + IGDB-compatible age_ratings
        content_ratings = gog_meta.get("content_ratings") or []
        required_age = gog_meta.get("age_rating")
        age_ratings = []
        if content_ratings:
            max_age = 0
            for cr in content_ratings:
                if isinstance(cr, dict):
                    system = cr.get("name", "")
                    age = cr.get("ageRating")
                    if system and isinstance(age, (int, float)):
                        age_ratings.append({"system": system, "rating": str(int(age))})
                        if age > max_age:
                            max_age = int(age)
            if not required_age and max_age > 0:
                required_age = max_age

        return {
            "title": gog_meta.get("title", ""),
            "short_description": short_desc,
            "description": gog_meta.get("description", ""),
            "header_url": hero,
            "cover": cover,
            "hero": hero,
            "logo_url": _fix_gog_cdn_url(gog_meta.get("logo_url", "")),
            "icon_url": _fix_gog_cdn_url(gog_meta.get("icon_url", "")),
            "screenshots": [_fix_gog_cdn_url(s) for s in (gog_meta.get("screenshots") or [])],
            "release_date": release_dates_dict if release_dates_dict else release_date_str,
            "developers": gog_meta.get("developers", []),
            "publishers": gog_meta.get("publishers", []),
            "genres": gog_meta.get("genres", []),
            "features": features,
            "platforms": platforms,
            "type": gog_meta.get("type", ""),
            "slug": slug,
            "is_free": (
                gog_meta.get("is_free", False)
                or gog_meta.get("price_json", {}).get("final") == "$0.00"
            ),
            "price": gog_meta.get("price", ""),
            "rating": (gog_meta.get("rating", 0) or 0) * 2,  # GOG stars*10 → 0-100
            "supported_languages": supported_languages,
            "full_audio_languages": full_audio_languages,
            "videos": gog_meta.get("videos", []),
            # New fields
            "tags": gog_meta.get("tags", []),
            "franchise": gog_meta.get("series_name", ""),
            "required_age": required_age,
            "age_ratings": age_ratings,
            "content_ratings": content_ratings,
            "editions": gog_meta.get("editions", []),
            "game_modes_detail": game_modes_detail if game_modes_detail else {},
            "controller_support": controller_support,
            "links": links_list,
            "dlcs": gog_meta.get("dlcs", []),
            "changelog": gog_meta.get("changelog", ""),
        }

