    def _apply_catalog_data(
        self, existing: GogGame, catalog: Dict[str, Any]
    ) -> None:
        """Apply catalog API data to an existing game record.

        Catalog is the **authoritative source** within the GOG plugin —
        always overwrites when it has data. This ensures the freshest,
        highest-quality data from GOG's own catalog API is used.
        """
        # Images — catalog always overwrites
        if catalog.get("cover_vertical_url"):
            existing.cover_vertical_url = catalog["cover_vertical_url"]
        if catalog.get("cover_horizontal_url"):
            existing.cover_horizontal_url = catalog["cover_horizontal_url"]
        if catalog.get("logo_url"):
            existing.logo_url = catalog["logo_url"]
        if catalog.get("galaxy_background_url"):
            existing.galaxy_background_url = catalog["galaxy_background_url"]
        if catalog.get("screenshots"):
            existing.screenshots = catalog["screenshots"]

        # Metadata — catalog always overwrites
        if catalog.get("tags"):
            existing.tags = catalog["tags"]
        if catalog.get("features"):
            existing.features = catalog["features"]
        if catalog.get("genres"):
            existing.genres = catalog["genres"]
        if catalog.get("developers"):
            existing.developers = catalog["developers"]
        if catalog.get("publishers"):
            existing.publishers = catalog["publishers"]

        # Content ratings & pricing (new fields)
        if catalog.get("content_ratings"):
            existing.content_ratings = catalog["content_ratings"]
        if catalog.get("price_json"):
            existing.price_json = catalog["price_json"]
        if catalog.get("product_state"):
            existing.product_state = catalog["product_state"]

        # Ratings
        if catalog.get("reviews_rating") is not None:
            existing.rating = catalog["reviews_rating"]
        if catalog.get("reviews_count") is not None:
            existing.reviews_count = catalog["reviews_count"]

        # Links
        if catalog.get("store_link"):
            existing.store_link = catalog["store_link"]

        # Editions
        if catalog.get("editions"):
            existing.editions = catalog["editions"]

        # Dates
        if catalog.get("global_release_date"):
            rd = str(catalog["global_release_date"])
            existing.global_release_date = rd.split("T")[0] if "T" in rd else rd[:10]
        if catalog.get("release_date"):
            rd = str(catalog["release_date"])
            existing.release_date = rd.split("T")[0] if "T" in rd else rd[:10]
        if catalog.get("store_release_date"):
            rd = str(catalog["store_release_date"])
            existing.store_release_date = rd.split("T")[0] if "T" in rd else rd[:10]

        # Platform support from catalog
        os_list = catalog.get("operating_systems", [])
        if os_list:
            existing.windows = "windows" in os_list
            existing.mac = "osx" in os_list or "mac" in os_list
            existing.linux = "linux" in os_list

        # Languages from catalog
        lang_data = catalog.get("languages_data")
        if isinstance(lang_data, dict) and lang_data:
            # Build localizations-style list from userPreferredLanguage
            code = lang_data.get("code", "")
            in_audio = lang_data.get("inAudio", False)
            in_text = lang_data.get("inText", False)
            if code and not existing.localizations:
                existing.localizations = [{
                    "code": code,
                    "name": code,
                    "text": in_text,
                    "audio": in_audio,
                }]

        existing.catalog_enriched = True
        existing.data_source = "catalog"
        existing.last_updated = utc_now()

    async def fetch_game_metadata(
        self, app_ids: List[str], download_images: bool = False
    ) -> List[Game]:
        """Fetch game metadata from catalog database, with GOG products API fallback.

        Sync flow:
        1. Look up game in local catalog cache (catalog.db)
        2. If not in cache, fetch from GOG products API and cache locally
        3. Return Game objects for all found games

        Args:
            app_ids: List of GOG IDs (as strings)
            download_images: If True, download images (not implemented yet)

        Returns:
            List of Game objects
        """
        db = self._get_db()
        gogids = [int(app_id) for app_id in app_ids]

        games = []
        skipped = 0
        for gogid in gogids:
            gog_game = db.get_game(gogid)
            source = "cache"

            # If not in local cache, fetch from GOG products API
            if gog_game is None:
                logger.info(f"Game {gogid} not in cache, fetching from GOG products API")
                gog_game = await self._fetch_from_products_api(gogid)
                source = "gog_api"

            # Per-game catalog fallback for new purchases between scans
            if gog_game and not gog_game.cover_vertical_url and not gog_game.catalog_enriched:
                try:
                    api = self._get_api_client()
                    catalog_data = await api.fetch_catalog_for_game(gogid, gog_game.title)
                    if catalog_data:
                        self._apply_catalog_data(gog_game, catalog_data)
                        db.commit()
                        source = "catalog_fallback"
                except Exception as e:
                    logger.debug(f"Per-game catalog fallback failed for {gogid}: {e}")

            if gog_game:
                game = self._convert_to_plugin_game(gog_game)
                games.append(game)
                logger.debug(f"Added game {gogid} ({gog_game.title}) from {source}")
            else:
                logger.warning(f"SKIPPED: Could not find metadata for GOG game {gogid} - not adding to sync")
                skipped += 1

        logger.info(f"fetch_game_metadata: {len(games)} added, {skipped} skipped (no metadata)")
        return games

    async def _fetch_from_products_api(self, gogid: int) -> Optional[GogGame]:
        """Fetch a single game from GOG's public products API and cache it.

        Args:
            gogid: GOG product ID

        Returns:
            GogGame from local DB (after caching) or None
        """
        from .gog_api import GogApiClient
        from .database import GogGame as GogGameModel

        api = self._get_api_client()
        metadata = await api.get_product_metadata(gogid)
        if not metadata:
            return None

        db = self._get_db()

        # Create or update GogGame record
        existing = db.get_game(gogid)
        if existing:
            # Enrich existing record with products API data
            self._apply_products_api_data(existing, metadata)
        else:
            game = GogGameModel(
                gogid=gogid,
                title=metadata.get("title", "Unknown"),
                slug=metadata.get("slug", ""),
                type=metadata.get("game_type", "game"),
                description=metadata.get("description"),
                description_lead=metadata.get("description_lead"),
                description_cool=metadata.get("description_cool"),
                background_url=metadata.get("background_url"),
                logo_url=metadata.get("logo_url"),
                icon_url=metadata.get("icon_url"),
                windows=metadata.get("windows", False),
                mac=metadata.get("mac", False),
                linux=metadata.get("linux", False),
                release_date=metadata.get("release_date"),
                data_source="gog_api_full",
                gogdb_imported=False,
                enriched=True,
            )
            game.screenshots = metadata.get("screenshots", [])
            game.downloads_json = metadata.get("downloads_json", {})
            game.dlcs = metadata.get("dlcs", [])
            game.videos = metadata.get("videos", [])
            game.languages = metadata.get("languages", {})
            game.links = metadata.get("links", {})
            game.changelog = metadata.get("changelog")
            db.session.add(game)

        try:
            db.commit()
        except Exception as e:
            logger.warning(f"Failed to cache product {gogid}: {e}")
            db.rollback()
            return None
        logger.info(f"Cached product from GOG API: {gogid}")
        return db.get_game(gogid)

