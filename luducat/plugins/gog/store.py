    def get_tag_sync_data(self) -> Optional[Dict[str, Any]]:
        """Return GOG user tag and favorite mappings for game_service to apply.

        GOG's "FAVORITE" tag maps to luducat's native is_favorite flag.
        Other user tags map to luducat UserTag entries.

        Returns:
            Dict with "mode" and "mappings", or None if tag sync disabled
        """
        db = self._get_db()

        # Query all GOG games that have user_tags set
        from .database import GogGame as GogGameModel
        from sqlalchemy import and_

        games_with_tags = db.session.query(GogGameModel).filter(
            and_(
                GogGameModel._user_tags.isnot(None),
                GogGameModel._user_tags != "[]",
            )
        ).all()

        if not games_with_tags:
            return None

        mappings = {}
        for game in games_with_tags:
            tags = game.user_tags  # List[str] from JSON property
            if not tags:
                continue

            # Separate FAVORITE from regular tags
            is_favorite = "FAVORITE" in tags
            regular_tags = [t for t in tags if t != "FAVORITE"]

            mappings[str(game.gogid)] = {
                "tags": regular_tags,
                "favorite": is_favorite,
            }

        if not mappings:
            return None

        return {
            "mode": "add_only",
            "mappings": mappings,
        }

